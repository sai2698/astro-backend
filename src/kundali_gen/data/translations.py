def t(text, lang="en"):
    """Translate text to the given language code. Fallback to English if not found."""
    if not text:
        return text
    if lang == "en":
        return text
    
    lang_dict = TRANSLATIONS.get(lang, {})
    
    # Fast exact match
    if text in lang_dict:
        return lang_dict[text]
        
    translated = str(text)

    # Handle specific composite string suffixes/patterns
    if "Ashtakavarga" in translated:
        translated = translated.replace("Ashtakavarga", "అష్టకవర్గ")
        for k in lang_dict.keys():
            if k in translated and k in ["Sun", "Moon", "Mars", "Mercury", "Jupiter", "Venus", "Saturn"]:
                translated = translated.replace(k, lang_dict[k])
        return translated
        
    if "Mahadasha Overview" in translated:
        translated = translated.replace(" Mahadasha Overview", " మహాదశ అవలోకనం")
        for k in lang_dict.keys():
            if k in translated and k in ["Sun", "Moon", "Mars", "Mercury", "Jupiter", "Venus", "Saturn", "Rahu", "Ketu"]:
                translated = translated.replace(k, lang_dict[k])
        return translated
        
    if "Mahadasha Predictions" in translated:
        translated = translated.replace(" Mahadasha Predictions", " మహాదశ ఫలాలు")
        for k in lang_dict.keys():
            if k in translated and k in ["Sun", "Moon", "Mars", "Mercury", "Jupiter", "Venus", "Saturn", "Rahu", "Ketu"]:
                translated = translated.replace(k, lang_dict[k])
        return translated

    if " Sign" in translated:
        translated = translated.replace(" Sign", " రాశి")
        for k in lang_dict.keys():
            if k in translated:
                translated = translated.replace(k, lang_dict[k])
        return translated

    # Handle hyphens like "Krishna-Purnima/Amavasya" or "Dhanishtha-4"
    if "-" in translated:
        parts = translated.split("-")
        return "-".join([lang_dict.get(p, p) for p in parts])
        
    # Handle planet degree strings in charts like "Su 13 &copy;"
    parts = translated.split(" ")
    if len(parts) > 1 and parts[0] in lang_dict:
        parts[0] = lang_dict[parts[0]]
        return " ".join(parts)

    return translated
